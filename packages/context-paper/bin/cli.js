#!/usr/bin/env node
'use strict';

const { Command } = require('commander');
const path = require('path');
const fs = require('fs');
const pkg = require('../package.json');

const program = new Command();

program
  .name('context-paper')
  .description('Academic paper forge')
  .version(pkg.version);

// ─── forge ─────────────────────────────────────────────────────────
program
  .command('forge')
  .description('Forge paper from structured input')
  .requiredOption('-i, --input <path>', 'Input file or directory')
  .option('-o, --output <path>', 'Output file or directory')
  .option('-f, --format <type>', 'Output format', 'json')
  .option('--dry-run', 'Simulate without writing files')
  .option('--domain <name>', 'Domain filter')
  .action((opts) => {
    if (opts.input && !fs.existsSync(path.resolve(opts.input))) {
      console.error('Error: Input file not found: ' + path.resolve(opts.input));
      process.exit(1);
    }
    if (opts.dryRun) {
      console.log('[context-paper] forge --dry-run: validated, no output written.');
      return;
    }
    if (opts.output) {
      const outDir = path.dirname(path.resolve(opts.output));
      if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
      const result = {
        command: 'forge',
        package: 'context-paper',
        timestamp: new Date().toISOString(),
        status: 'completed',
        input: opts.input || null,
      };
      fs.writeFileSync(path.resolve(opts.output), JSON.stringify(result, null, 2), 'utf8');
      console.log('Output written to ' + opts.output);
    } else {
      const result = { status: 'ok', package: 'context-paper', command: 'forge' };
      if (opts.format === 'json') {
        process.stdout.write(JSON.stringify(result, null, 2));
      } else {
        console.log('[context-paper] forge: completed');
      }
    }
  });

// ─── verify ────────────────────────────────────────────────────────
program
  .command('verify')
  .description('Verify paper structure')
  .requiredOption('-i, --input <path>', 'Input file or directory')
  .option('-o, --output <path>', 'Output file or directory')
  .option('-f, --format <type>', 'Output format', 'json')
  .option('--dry-run', 'Simulate without writing files')
  .option('--domain <name>', 'Domain filter')
  .action((opts) => {
    if (opts.input && !fs.existsSync(path.resolve(opts.input))) {
      console.error('Error: Input file not found: ' + path.resolve(opts.input));
      process.exit(1);
    }
    if (opts.dryRun) {
      console.log('[context-paper] verify --dry-run: validated, no output written.');
      return;
    }
    if (opts.output) {
      const outDir = path.dirname(path.resolve(opts.output));
      if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
      const result = {
        command: 'verify',
        package: 'context-paper',
        timestamp: new Date().toISOString(),
        status: 'completed',
        input: opts.input || null,
      };
      fs.writeFileSync(path.resolve(opts.output), JSON.stringify(result, null, 2), 'utf8');
      console.log('Output written to ' + opts.output);
    } else {
      const result = { status: 'ok', package: 'context-paper', command: 'verify' };
      if (opts.format === 'json') {
        process.stdout.write(JSON.stringify(result, null, 2));
      } else {
        console.log('[context-paper] verify: completed');
      }
    }
  });

// ─── compile ───────────────────────────────────────────────────────
program
  .command('compile')
  .description('Compile paper to output format')
  .requiredOption('-i, --input <path>', 'Input file or directory')
  .option('-o, --output <path>', 'Output file or directory')
  .option('-f, --format <type>', 'Output format', 'json')
  .option('--dry-run', 'Simulate without writing files')
  .option('--domain <name>', 'Domain filter')
  .action((opts) => {
    if (opts.input && !fs.existsSync(path.resolve(opts.input))) {
      console.error('Error: Input file not found: ' + path.resolve(opts.input));
      process.exit(1);
    }
    if (opts.dryRun) {
      console.log('[context-paper] compile --dry-run: validated, no output written.');
      return;
    }
    if (opts.output) {
      const outDir = path.dirname(path.resolve(opts.output));
      if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });
      const result = {
        command: 'compile',
        package: 'context-paper',
        timestamp: new Date().toISOString(),
        status: 'completed',
        input: opts.input || null,
      };
      fs.writeFileSync(path.resolve(opts.output), JSON.stringify(result, null, 2), 'utf8');
      console.log('Output written to ' + opts.output);
    } else {
      const result = { status: 'ok', package: 'context-paper', command: 'compile' };
      if (opts.format === 'json') {
        process.stdout.write(JSON.stringify(result, null, 2));
      } else {
        console.log('[context-paper] compile: completed');
      }
    }
  });

// Error handling for unknown commands
program.on('command:*', (operands) => {
  console.error('Error: Unknown command "' + operands[0] + '".');
  program.outputHelp();
  process.exit(1);
});

program.parse(process.argv);

if (program.args.length === 0 && process.argv.length <= 2) {
  program.outputHelp();
}
